def main() -> int:
    if not PLAYBOOK_DOCX.exists():
        print(f"ERROR: {PLAYBOOK_DOCX} not found", file=sys.stderr)
        return 1

    doc = Document(PLAYBOOK_DOCX)
    shared_ops = _read(SRC_DIR / "NDA Review Skill" / "references" / "shared_operating_rules.md")
    sections = _extract_sections(shared_ops)
    no_sig = _read(SRC_DIR / "NDA Review Skill" / "references" / "no_signature_checklist.md")

    # Wipe and regenerate playbook/ — keeps deletes / renames honest.
    if OUT_DIR.exists():
        shutil.rmtree(OUT_DIR)
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    written = 0

    # global/
    global_files = _build_global_files(doc, sections, no_sig)
    for name, content in global_files.items():
        _write(OUT_DIR / "global" / name, content)
        written += 1

    # per-type
    for t in TYPES:
        type_files = _build_type_files(doc, t)
        for name, content in type_files.items():
            _write(OUT_DIR / t / name, content)
            written += 1

    # docs/playbook_cross_reference.md
    DOCS_DIR.mkdir(parents=True, exist_ok=True)
    _write(DOCS_DIR / "playbook_cross_reference.md", _build_cross_reference())

    print(f"build_playbook.py: wrote {written} bundle files + cross-reference")
    print(f"  global/: {len(global_files)} files")
    print(f"  per-type: {len(TYPES)} folders, {2 * len(TYPES)} files")
    print(f"  cross-reference: docs/playbook_cross_reference.md")
    return 0


if __name__ == "__main__":
    sys.exit(main())
