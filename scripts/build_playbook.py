def _md_escape_cell(s: str) -> str:
    """Make a cell safe for a markdown table row."""
    return s.replace("|", "\\|").replace("\n", "<br>").strip()


def _table_to_markdown(table) -> str:
    """Render a python-docx table as a GFM markdown table."""
    rows = table.rows
    if not rows:
        return ""
    header = [_md_escape_cell(c.text) for c in rows[0].cells]
    sep = ["---"] * len(header)
    body_rows = []
    for row in rows[1:]:
        cells = [_md_escape_cell(c.text) for c in row.cells]
        # Pad if a row has fewer cells than the header (defensive).
        while len(cells) < len(header):
            cells.append("")
        body_rows.append("| " + " | ".join(cells) + " |")
    lines = ["| " + " | ".join(header) + " |", "| " + " | ".join(sep) + " |"]
    lines.extend(body_rows)
    return "\n".join(lines) + "\n"


def _extract_sections(text: str) -> dict[str, str]:
    """Split a markdown doc by `## ` headings; return {heading_text: section_body}.

    Section body excludes the heading line; trailing whitespace stripped.
    """
    sections: dict[str, str] = {}
    matches = list(re.finditer(r"^##\s+(.+)$", text, re.M))
    for i, m in enumerate(matches):
        name = m.group(1).strip()
        start = m.end()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        sections[name] = text[start:end].strip()
    return sections


def _render_ai_review_procedure(doc) -> str:
    """Render Section 10 (AI Review Procedure) — paragraphs + headings + bullets — as markdown."""
    para_map = {p._element: p for p in doc.paragraphs}
    in_section = False
    out: list[str] = []
    for element in doc.element.body:
        if element.tag != qn("w:p"):
            continue
        p = para_map.get(element)
        if p is None:
            continue
        text = p.text.strip()
        if not text:
            continue
        style = p.style.name if p.style else ""
        if style == "Heading 1":
            if text == AI_PROC_START:
                in_section = True
                continue  # heading itself becomes the file title
            if text == AI_PROC_END:
                break
        if not in_section:
            continue
        if style == "Heading 2":
            out.append(f"\n### {text}\n")
        elif style == "List Bullet":
            out.append(f"- {text}")
        else:
            # Normal paragraph. The docx uses `\n` for soft breaks inside one paragraph
            # (e.g. the issue-list schema). Preserve them as blank-line-separated lines
            # so markdown renders them as separate output-schema fields rather than
            # one continuous paragraph.
            out.append(text.replace("\n", "  \n"))
            out.append("")  # blank line between paragraphs
    return "\n".join(out).strip() + "\n"


def _read(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def _write(path: Path, content: str) -> bool:
    """Write content if changed. Returns True if file was created or modified."""
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and path.read_text(encoding="utf-8") == content:
        return False
    path.write_text(content, encoding="utf-8")
    return True


def _clean_dir(d: Path) -> None:
    """Remove every .md under d (recursively) so removed inputs don't linger."""
    if not d.exists():
        return
    for p in d.rglob("*.md"):
        p.unlink()


