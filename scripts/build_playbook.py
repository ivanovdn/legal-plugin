def _build_type_files(doc, contract_type: str) -> dict[str, str]:
    """Build playbook/<type>/{SKILL.md, playbook_matrix.md}."""
    folder = SRC_DIR / TYPE_TO_FOLDER[contract_type]
    skill_md = _read(folder / "SKILL.md")
    matrix_md = _table_to_markdown(doc.tables[TABLE_INDEX[TYPE_TO_MATRIX_TABLE[contract_type]]])
    matrix_doc = (
        GENERATED_NOTICE
        + f"# {contract_type.upper()} Playbook Matrix\n\n"
        + "Reference table from the Trinetix Contract Playbook. Use these as the "
        + "deterministic clause-by-clause ceiling alongside the per-type SKILL.md.\n\n"
        + matrix_md
    )
    return {
        "SKILL.md": GENERATED_NOTICE + skill_md.strip() + "\n",
        "playbook_matrix.md": matrix_doc,
    }


